'''ESTRUTURA DE REPETIÇÃO (LAÇO DE REPETIÇÕES) PARTE 1'''
print('contando com 6 passos')
for c in range(0, 6): #contando com 6 passos
    print(c)
print('END')

print('contando do 1 ao 6')
for c in range(1, 7): #contando do 1 ao 6
    print(c)
print('END ')

print('contando no sentido inverso')
for c in range(6, 0, -1): #contando no sentido inverso
    print(c)
print('END')

print('contando de 2 em 2 passos')
for c in range(0, 7, 2): #contando de 2 em 2 passos
    print(c)
print('END')
